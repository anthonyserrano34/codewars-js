function multiply(a, b) {
	return a * b;
}

console.log(multiply(1, 1)); // Expected: 1
console.log(multiply(2, 1)); // Expected: 2
console.log(multiply(2, 2)); // Expected: 4
console.log(multiply(3, 5)); // Expected: 15
console.log(multiply(5, 0)); // Expected: 0
console.log(multiply(0, 5)); // Expected: 0
console.log(multiply(0, 0)); // Expected: 0